#include "ft_printf.h"

void	ft_putlongnbr(long n)
{
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = -n;
		}
		if (n >= 10)
		{
			ft_putlongnbr(n / 10);
			ft_putlongnbr(n % 10);
		}
		else
			ft_putchar(n + '0');
	}
}

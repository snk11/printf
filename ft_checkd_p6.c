#include "ft_printf.h"

int		ft_countd4a2z(t_numb *e, t_count *f)
{
	int	r1;

	r1 = 0;
	if (e->indplus == 1)
	{
		while (e->w - f->i + 1 > 0)
		{
			ft_putchar(' ');
			r1++;
			e->w--;
		}
	}
	else
	{
		while (e->w - f->i > 0)
		{
			ft_putchar(' ');
			r1++;
			e->w--;
		}
	}
	return (r1);
}

#include "ft_printf.h"

void		ft_p2(char **str, t_numb *e)
{
	if (**str != 0)
	{
		if (e->indll == 1)
		{
			(**str)++;
			(**str)++;
			e->indll = 0;
		}
		else
			(*str)++;
	}
}

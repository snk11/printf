void	ft_initialize(t_numb **e)
{
	(*e)->w = 0;
	(*e)->pr = 0;
	(*e)->indzero = 0;
	(*e)->indpr = 0;
	(*e)->indplus = 0;
	(*e)->indspace = 0;
	(*e)->indminus = 0;
}

#include "ft_printf.h"

void	ft_putunbr(unsigned int n)
{
	if (n == UINT_MAX)
		ft_putstr("4294967295");
	else
	{
		if (n >= 10)
		{
			ft_putunbr(n / 10);
			ft_putunbr(n % 10);
		}
		else
			ft_putchar(n + '0');
	}
}

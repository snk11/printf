int		ft_checkletter(char *str)
{
	if (*str == 'd' || *str == 'D' || *str == 'i'|| *str == 'o'
			|| *str == 'O' || *str == 'u' || *str == 'U' || *str == 'c'
			|| *str == 'C'|| *str == 'p' || *str == 's'  || *str == 'S'
			|| *str == 'R' || *str == 'x' || *str == 'X')
		return (1);
	return (0);
}

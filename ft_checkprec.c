#include "ft_printf.h"

int		ft_checkprec(char *s)
{
	int		i;
	char	*begi;

	i = 0;
	while (*s)
	{
		s++;
		i++;
	}
	s--;
	while ((*s == ' ' || (*s >= '0' && *s <= '9')) && i > 0)
	{
		i--;
		s--;
	}
	begi = &s[1];
	if (*s == '.')
	{
		return (ft_atoi(begi));
	}
	return (0);
}

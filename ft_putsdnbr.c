#include "ft_printf.h"

void	ft_putsdnbr(short int n)
{
	if (n == -32768)
		ft_putstr("-32768");
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = -n;
		}
		if (n >= 10)
		{
			ft_putsdnbr(n / 10);
			ft_putsdnbr(n % 10);
		}
		else
			ft_putchar(n + '0');
	}
}

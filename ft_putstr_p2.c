#include "ft_printf.h"

void	ft_putstrad3a(int pr, char const *s, int i)
{
	int		n;

	n = 0;
	if (i < pr && i >= 1)
	{
		while (n < pr - i)
		{
			ft_putchar('0');
			n++;
		}
	}
	i = 0;
	while (s[i])
	{
		ft_putchar(s[i]);
		i++;
	}
}

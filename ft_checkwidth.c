# include "ft_printf.h"

int		ft_checkwidth(char *s)
{
	int	i;
	char *begi;

	i = 0;
	begi = s;
	if ((s[0] >= '0' && s[0] <= '9') || s[0] == ' ' || s[0] == '+' || s[0] == '-')
	{
		while((s[0] >= '0' && s[0] <= '9') || s[0] == ' ' || s[0] == '+' || s[0] == '-')
		{
			s++;
			i++;
		}
		s[i] = 0;
		return (ft_atoi(begi));
	}
	if (ft_checkprec(s) == 0)
	{
		while(*s)
		{
			s++;
			i++;
		}
		s--;
		while ((*s == ' ' || *s == '-' || (*s >= '0' && *s <= '9')) && i > 0)
		{
			s--;
			i--;
		}
		s++;
//		if (*s == ' ' || (*s >= '0' && *s <= '9'))
			return ft_atoi(s);
	}
	else
	{
		
	}
	return 0;
}

